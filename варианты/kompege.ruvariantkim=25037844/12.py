def alg(n):
    while ('>1' in n) or ('>2' in n) or ('>3' in n):
        if '>1' in n:
            n = n.replace('>1', '22>')
        if '>2' in n:
            n = n.replace('>2', '222>')
        if '>3' in n:
            n = n.replace('>3', '1>')
    return n

